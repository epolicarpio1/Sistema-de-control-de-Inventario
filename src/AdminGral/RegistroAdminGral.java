    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        Conexion cc = new Conexion();
        Connection cn = cc.getConnection();
     
        
        String nom_admi,ape_admi,tipo_invi,no_ficha,fecha,edificio,tipo_sesion,contra_usu;
        String sql="";
        nom_admi=nombre.getText();
        contra_usu=contra.getText();
        ape_admi=apellido.getText();
        fecha=fech.getText();
        no_ficha=fich.getText();
        tipo_sesion=combotipo.getSelectedItem().toString();
        edificio=comboedificio.getSelectedItem().toString();
        tipo_invi=emple.getSelectedItem().toString();
        //campos obligatorios
        if(combotipo.getSelectedItem().toString().equals("")){
         JOptionPane.showMessageDialog(null,"Los Campos Marcados (*) Son Necesarios!","ERROR",JOptionPane.ERROR_MESSAGE,save);   
        }
        if(nombre.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Los Campos Marcados (*) Son Necesarios!","ERROR",JOptionPane.ERROR_MESSAGE,save);
             return;        
              }
        if(fich.getText().equals("")){
             JOptionPane.showMessageDialog(null,"Los Campos Marcados (*) Son Necesarios!","ERROR",JOptionPane.ERROR_MESSAGE,save);
             return;        
              }    
        if(contra.getText().equals("")){
             JOptionPane.showMessageDialog(null,"Los Campos Marcados (*) Son Necesarios!","ERROR",JOptionPane.ERROR_MESSAGE,save);
             return;        
              }
        if(recontra.getText().equals("")){
             JOptionPane.showMessageDialog(null,"Los Campos Marcados (*) Son Necesarios!","ERROR",JOptionPane.ERROR_MESSAGE,save);
             return;        
              }
        //sentencia SQL
        sql="INSERT INTO administrador(nom_admi,ape_admi,tipo_invi,no_ficha,fecha,edificio,tipo_sesion,contra_usu) VALUES (?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement psd =cn.prepareStatement(sql);
            psd.setString(1, nom_admi);
            psd.setString(2, ape_admi);
            psd.setString(3, tipo_invi);
            psd.setString(4,no_ficha);
            psd.setString(5,fecha);
            psd.setString(6,edificio);
            psd.setString(7, tipo_sesion);
            psd.setString(8, contra_usu);
           
           if(contra.getText() == null ? recontra.getText() == null : contra.getText().equals(recontra.getText())){      
           int n=psd.executeUpdate();
           if (n>0){
             cargar();
             JOptionPane.showMessageDialog(null,"Administrador Autorizado!","REGISTRADO",JOptionPane.PLAIN_MESSAGE,icon);
              
               limpiar();
               bloquear();
            }
           
           }else{
     JOptionPane.showMessageDialog(null,"Las contraseñas no coinciden, VERIFIQUE!","ERROR",JOptionPane.ERROR_MESSAGE,save);
               } 
        } catch (SQLException ex) {
            Logger.getLogger(RegistroAdminGral.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_guardarActionPerformed

