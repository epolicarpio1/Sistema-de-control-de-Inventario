      void cargar(){           
     String [] titulos ={"Nombre","Apellido","Tipo","No.Ficha","Dia de Registro","Edificio Laboral","Tipo de sesion","Contraseña"};
 String sql = "SELECT * FROM administrador ";

 model= new DefaultTableModel(null,titulos);
 Conexion cc= new Conexion();
 Connection cn = cc.getConnection();

        try {
          Statement   st = cn.createStatement();
          ResultSet rs=st.executeQuery(sql);
          
          String [] fila = new String[15];

  if(rs.next()){
    
            fila[0] = rs.getString("nom_admi");
            fila[1] = rs.getString("ape_admi");
            fila[2] = rs.getString("tipo_invi");
            fila[3] = rs.getString("no_ficha");
            fila[4] = rs.getString("fecha"); 
            fila[5] = rs.getString("edificio");
            fila[6] = rs.getString("tipo_sesion");
            fila[7] = rs.getString("contra_usu");
            
      model.addRow(fila);

  }

        tabla.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(RegistroAdminGral.class.getName()).log(Level.SEVERE, null, ex);
        }

      }       
    

